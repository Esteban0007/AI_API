def generate_movie_dataset(limit: int = None) -> list:
    """Generate movie documents from real movies only (no cycling)."""
    # If limit is None or greater than available movies, use all real movies
    if limit is None or limit > len(REAL_MOVIES):
        limit = len(REAL_MOVIES)

    print(f"🎬 Generating {limit} movie documents (real movies only)...")

    documents = []

    for i, movie in enumerate(REAL_MOVIES[:limit]):
        doc_id = i + 1

        # Create document
        doc = {
            "id": f"movie-{doc_id:06d}",
            "title": movie["title"],
            "content": movie["overview"],
            "keywords": [g.lower() for g in movie["genres"]],
            "metadata": {
                "category": movie["genres"][0],
                "genres": movie["genres"],
                "year": movie["year"],
                "rating": movie["rating"],
                "source": "curated-film-database",
                "created_at": datetime.utcnow().isoformat(),
            },
        }
        documents.append(doc)

    return documents


def main():
    parser = argparse.ArgumentParser(
        description="Generate movie dataset from curated film database."
    )
    parser.add_argument(
        "--count",
        type=int,
        default=None,
        help="Number of movies to generate. If not specified, generates all real movies available",
    )
    parser.add_argument(
        "--output",
        type=str,
        default="data/movies_dataset.json",
        help="Output JSON file path",
    )

    args = parser.parse_args()

    documents = generate_movie_dataset(args.count)

    # Ensure output directory exists
    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Write JSON wrapped in {"documents": [...]}
    output_data = {"documents": documents}
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(output_data, f, ensure_ascii=False, indent=2)

    print(f"✅ Generated {len(documents)} movie documents")
    print(f"📄 Saved to: {output_path}")
    print(f"\n📊 Sample:")
    print(f"   Title: {documents[0]['title']}")
    print(f"   Category: {documents[0]['metadata']['category']}")
    print(f"   Genres: {', '.join(documents[0]['keywords'])}")


if __name__ == "__main__":
    main()
